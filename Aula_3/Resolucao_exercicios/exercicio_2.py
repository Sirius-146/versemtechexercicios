valor = int(input('Insira um número: ')) #Recebe um valor numérico

positivo = valor > 0 #Testa se o número é positivo (maior que zero)

if positivo: #Testa se o retorno é verdadeiro
  print('O número é positivo') #Imprime o valor na tela
else: #Caso o valor seja falso
  print('O número é negativo') #Imprime o valor na tela